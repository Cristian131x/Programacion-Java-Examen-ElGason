    void validarTabla() {
        for (int i = 0; i < xtablaVenta.getRowCount(); i++) {
            if (xtablaVenta.getValueAt(i, 0).equals(xgasito.getSelectedItem())) {
                JOptionPane.showMessageDialog(null, "Gas ya ingresado ingrese otro");
                modelo.removeRow(i);
               
            }
           
        }
        rellenarTabla();

    }

