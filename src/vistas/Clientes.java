    public int IdComuna() {
        ResultSet rs = null;
        PreparedStatement ps = null;
        int ID = 0;
        String sql = "SELECT id_comuna FROM comuna WHERE nombre=?";
        try {
            ps = Conexion.Conectar().prepareStatement(sql);
            ps.setString(1, xcomunaC.getSelectedItem().toString());
            rs = ps.executeQuery();
            while (rs.next()) {
                ID = rs.getInt(1);
            }
        } catch (Exception e) {
            System.out.println("me mori");
        }
        return ID;
    }
