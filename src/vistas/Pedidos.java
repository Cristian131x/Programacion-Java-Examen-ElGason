    void listarPedidos() {
        if (xfecha.getDate() == null || xfecha2.getDate() == null) {
            Calendar calendar = new GregorianCalendar(0001, 01, 01);
            Calendar calendar2 = new GregorianCalendar(9998, 12, 31);
            Date Fecha = calendar.getTime();
            Date Fecha2 = calendar2.getTime();
            ped.setFecha(Fecha);
            ped.setFecha2(Fecha2);
            xlistar.setModel(cped.listarPedidosF(ped));
            xtotal.setText(Integer.toString(CalcularTotal()));
            MinMas();
        } else {
            Date Fecha = xfecha.getDate();
            ped.setFecha(Fecha);
            Date Fecha2 = xfecha2.getDate();
            ped.setFecha2(Fecha2);
            xlistar.setModel(cped.listarPedidosF(ped));
            xtotal.setText(Integer.toString(CalcularTotal()));
            MinMas();
        }
    }

