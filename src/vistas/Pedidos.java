    public int CalcularTotal() {
        int contar = xlistar.getRowCount();
        int suma = 0;
        for (int i = 0; i < contar; i++) {
            suma = suma + Integer.parseInt(xlistar.getValueAt(i, 2).toString());
        }
        return suma;
    }

    public void MinMas() {
        ArrayList<Integer> lista = new ArrayList<Integer>();
        for (int i = 0; i < xlistar.getRowCount(); i++) {
            lista.add(Integer.parseInt(xlistar.getValueAt(i, 2).toString()));
        }
        try {
            xmaximo.setText("0");
            xminimo.setText("0");
            int max = Collections.max(lista);
            int min = Collections.min(lista);
            xmaximo.setText(Integer.toString(max));
            xminimo.setText(Integer.toString(min));
        } catch (Exception e) {
        }

    }
