    public void validarUsuarios(){
        int resultado = 0;
        String password=String.valueOf(xpassword.getPassword());
        String usuario=xusuario.getText();
        String SQL="select * from usuario where usuario='"+usuario+"' and password='"+password+"'";
        try {
            Statement st=Conexion.coneccion().createStatement();
            ResultSet rs=st.executeQuery(SQL);
            if (rs.next()){
                resultado = 1;
                if (resultado == 1) {
                    Menu m =new Menu();
                    m.setVisible(true);
                    this.dispose();
                    JOptionPane.showMessageDialog(null,"INICIO SECCION EXITOSAMENTE");
                    limpiar();               
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Error Usuario y/o Contrase√±a no valido");
                limpiar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error");
        }
    }
   void limpiar() {
        xusuario.setText("");
        xpassword.setText("");
    }
   

